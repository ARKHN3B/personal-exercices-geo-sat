export enum Color {
	Primary = "primary", // - used to represent primary interface elements for a user. It's the color displayed most frequently across your app's screens and components.
	Secondary = "secondary", // - used to represent secondary interface elements for a user. It provides more ways to accent and distinguish your product. Having it is optional.
	Error = "error", // - used to represent interface elements that the user should be made aware of.
	Warning = "warning", // - used to represent potentially dangerous actions or important messages.
	Info = "info", // - used to present information to the user that is neutral and not necessarily important.
	Success = "success", // - used to indicate the successful completion of an action that user triggered.
}
